package Seminar5.Task02;

public class SongClass {
    String songName;
    String executor;
    String genre;
    String mood;
    Integer rating;

    public SongClass(String songName, String executor, String genre, String mood, Integer rating) {
        this.songName = songName;
        this.executor = executor;
        this.genre = genre;
        this.mood = mood;
        this.rating = rating;
    }
}
